    private void mostarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostarActionPerformed
        double maxProv=0;
        double minProv=10;
        
        double media;
        double suma = 0;
        
        for (int i=0; i<puntos.length;i++) {
            if (puntos[i] > maxProv){
                maxProv = puntos[i];              
            }
        }
        
        for (int j = 0; j < puntos.length; j++){
            if (puntos[j] < minProv) {
                minProv=puntos[j];
            }
        }
        
        for (int k=0; k < puntos.length; k++) {
            suma += puntos[k];
        }
        suma -= maxProv + minProv;
        media = suma / 3;
        
        area.setText("PuntuaciÃ³n media: " + format.format(media));
        
    }//GEN-LAST:event_mostarActionPerformed

